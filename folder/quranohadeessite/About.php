                <p class="lead">Before printing was widely adopted in the 19th century, the Quran was transmitted in manuscripts made by calligraphers and copyists. The earliest manuscripts were written in Ḥijāzī-typescript. The Hijazi style manuscripts nevertheless confirm that transmission of the Quran in writing began at an early stage. Probably in the ninth century, scripts began to feature thicker strokes, which are traditionally known as Kufic scripts. Toward the end of the ninth century, new scripts began to appear in copies of the Quran and replace earlier scripts. The reason for discontinuation in the use of the earlier style was that it took too long to produce and the demand for copies was increasing. Copyists would therefore choose simpler writing styles. Beginning in the 11th century, the styles of writing employed were primarily the naskh, muhaqqaq, rayḥānī and, on rarer occasions, the thuluth script. Naskh was in very widespread use. In North Africa and Iberia, the Maghribī style was popular. More distinct is the Bihari script which was used solely in the north of India. Nastaʻlīq style was also rarely used in Persian world.</p>
